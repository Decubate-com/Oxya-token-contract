function etherToWei(etherValue) {
    let weiValue = etherValue * 1e18;
    return BigInt(weiValue).toString(); // Use BigInt to deal with large numbers
}

function splitData(data) {
    let addresses = [];
    let amounts = [];

    for (let i = 0; i < data.length; i++) {
        let splitItem = data[i].split(',');
        addresses.push(splitItem[0]);
        amounts.push(splitItem[1]);
    }

    return [addresses, amounts];
}

